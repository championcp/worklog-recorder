// 提醒设置
export interface Reminder {
  id: string;
  title: string;
  description: string;
  type: 'task' | 'plan' | 'meeting' | 'deadline' | 'break' | 'custom';
  priority: 'low' | 'medium' | 'high' | 'urgent';
  triggerTime: Date;
  isRecurring: boolean;
  recurringPattern?: {
    type: 'daily' | 'weekly' | 'monthly' | 'yearly';
    interval: number; // 每隔多少个周期
    daysOfWeek?: number[]; // 周几 (0-6, 0为周日)
    dayOfMonth?: number; // 每月第几天
    endDate?: Date;
  };
  isActive: boolean;
  isCompleted: boolean;
  completedAt?: Date;
  snoozeUntil?: Date;
  notificationMethods: ('browser' | 'sound' | 'email')[];
  relatedTaskId?: string;
  relatedPlanId?: string;
  tags: string[];
  createdAt: Date;
  updatedAt: Date;
}

